    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String rollno=(String)jTextField7.getText();
        String name=(String)jTextField8.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String fathername=(String)jTextField9.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");//initialization and connection.
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Nidheesh#111");
            Statement st=con.createStatement();
            if(rollno.isEmpty())
            {
            JOptionPane.showMessageDialog(null,"Roll no can't be empty Please add rollno");
            }
               else
            {
                //push data in DB;
            st.executeUpdate("insert into student values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null,"Successfully added a student");
            setVisible(false);
            new adminHome().setVisible(true);//remove visibility the filled data
            }
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

