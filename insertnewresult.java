    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField5.getText();
         String physics=jTextField6.getText();
          String chemistry=jTextField4.getText();
           String maths=jTextField3.getText();
            String dbms=jTextField2.getText();
             String os=jTextField1.getText();
        try
        {
            
            Class.forName("com.mysql.jdbc.Driver");//initialization and connection.
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Nidheesh#111");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from student where rollno='"+ rollno +"'");
            if(rs.next())
            {
                st.executeUpdate("insert into result(rollno,physics,chemistry,maths,dbms,os) values('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+dbms+"','"+os+"')");
            
             JOptionPane.showMessageDialog(null,"Succesfully updated the student");
             setVisible(false);
             new insertnewresult().setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(null,"roll number does not exist");
            
        }
          catch(Exception e)
        {
           JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

